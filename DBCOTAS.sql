/* Definition for the `REPORTE_CALIDAD` view :  */

CREATE VIEW REPORTE_CALIDAD(
  MAXIMO,
  NOMINAL,
  IDCONTROL,
  MINIMO,
  IDPLANILLA,
  NOMBRE,
  PIEZA,
  ESCONTROL,
  VALORPIEZA,
  VALORPNP,
  NUMOP)
AS
SELECT 
  TOLERANCIAS.MAXIMO,
  TOLERANCIAS.NOMINAL,
  TOLERANCIAS.IDCONTROL,
  TOLERANCIAS.MINIMO,
  TOLERANCIAS.IDPLANILLA,
  CONTROLES.NOMBRE,
  VALORMEDICION.PIEZA,
  CONTROLES.ESCONTROL,
  VALORMEDICION.VALORPIEZA,
  VALORMEDICION.VALORPNP,
  PLANILLAMEDICION.NUMOP
FROM
  PLANILLAMEDICION
  LEFT OUTER JOIN TOLERANCIAS ON (PLANILLAMEDICION.ID = TOLERANCIAS.IDPLANILLA)
  LEFT OUTER JOIN VALORMEDICION ON (TOLERANCIAS.IDCONTROL = VALORMEDICION.IDCONTROL)
  AND (TOLERANCIAS.IDPLANILLA = VALORMEDICION.IDPLANILLA)
  INNER JOIN CONTROLES ON (TOLERANCIAS.IDCONTROL = CONTROLES.ID)
WHERE(PLANILLAMEDICION.NUMOP = 37019) and (VALORMEDICION.PIEZA = 1)  
ORDER BY
  TOLERANCIAS.ID;

