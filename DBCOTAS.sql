/* Definition for the `VISTA_MEDIDAS` view :  */

CREATE VIEW VISTA_MEDIDAS(
  ID,
  MAXIMO,
  NOMINAL,
  IDCONTROL,
  MINIMO,
  IDPLANILLA,
  NOMBRE,
  PIEZA,
  ESCONTROL,
  VALORPIEZA,
  VALORPNP,
  POSICION,
  IDINSTRUMENTO)
AS
SELECT distinct
  VALORMEDICION.ID,
  TOLERANCIAS.MAXIMO,
  TOLERANCIAS.NOMINAL,
  TOLERANCIAS.IDCONTROL,
  TOLERANCIAS.MINIMO,
  TOLERANCIAS.IDPLANILLA,
  CONTROLES.NOMBRE,
  VALORMEDICION.PIEZA,
  CONTROLES.ESCONTROL,
  VALORMEDICION.VALORPIEZA,
  VALORMEDICION.VALORPNP,
  VALORMEDICION.POSICION,
  VALORMEDICION.IDINSTRUMENTO
FROM
  TOLERANCIAS
  LEFT JOIN VALORMEDICION ON (TOLERANCIAS.IDCONTROL = VALORMEDICION.IDCONTROL)
  AND (TOLERANCIAS.IDPLANILLA = VALORMEDICION.IDPLANILLA)
  LEFT JOIN CONTROLES ON (TOLERANCIAS.IDCONTROL = CONTROLES.ID)
  LEFT JOIN PLANILLAMEDICION ON (PLANILLAMEDICION.IDPROCESO = TOLERANCIAS.IDPLANILLA)
ORDER BY
  VALORMEDICION.ID;

